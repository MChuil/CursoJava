package clase04;

public class Multiplicar {
    
    public static void main(String[] args){
        int numOne = 144, numTwo = 2;
        int result = 0;
        for(int counter = 1; counter <= numTwo; counter++){
                result += numOne;
        }
        System.out.println("Resultado: " + result);
    }
}

